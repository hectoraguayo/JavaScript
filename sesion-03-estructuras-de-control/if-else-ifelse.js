// If else + if else

let nota = 121;

if (nota === 5) {
    console.log("Sobresaliente");
} else if (nota === 4) {
    console.log("Buen trabajo, podrías mejorar");
} else if (nota === 3) {
    console.log("Has obtenido un suficiente");
} else if (nota === 2) {
    console.log("no has aprobado por poco");
} else if (nota === 1) {
    console.log("No has estudiado, trabaja más");
} else {
    console.log("Error, introduce una nota valida")
}